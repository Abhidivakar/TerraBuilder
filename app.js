// ── YAML SYNTAX HIGHLIGHTER ───────────────────
function highlightYaml(text) {
  // Escape HTML first
  const esc = s => s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');

  return text.split('\n').map(raw => {
    const line = esc(raw);

    // Comment lines
    if (/^\s*#/.test(raw)) return `<span class="yc">${line}</span>`;

    // YAML document separator ---
    if (/^---/.test(raw)) return `<span class="yd">${line}</span>`;

    // Key: value lines
    const kvMatch = raw.match(/^(\s*)([\w\-\.]+)(\s*:\s*)(.*)$/);
    if (kvMatch) {
      const [, indent, key, colon, rest] = kvMatch;
      const escapedIndent = esc(indent);
      const escapedKey = esc(key);
      const escapedColon = esc(colon);

      let coloredVal = '';
      if (!rest.trim()) {
        coloredVal = '';
      } else if (/^\s*\|/.test(rest) || /^\s*>/.test(rest)) {
        coloredVal = `<span class="yv">${esc(rest)}</span>`;
      } else if (/^\s*(true|false|yes|no|on|off)\s*$/i.test(rest)) {
        coloredVal = `<span class="yb">${esc(rest)}</span>`;
      } else if (/^\s*[\d.]+\s*$/.test(rest)) {
        coloredVal = `<span class="yn">${esc(rest)}</span>`;
      } else if (/^\s*["']/.test(rest)) {
        coloredVal = `<span class="yv">${esc(rest)}</span>`;
      } else if (/^\s*\$\{/.test(rest) || /^\s*\$\{\{/.test(rest)) {
        // Template vars — keep gold
        coloredVal = `<span class="ye">${esc(rest)}</span>`;
      } else {
        coloredVal = `<span class="yv">${esc(rest)}</span>`;
      }

      return `${escapedIndent}<span class="yk">${escapedKey}</span><span style="color:#d4d4d4">${escapedColon}</span>${coloredVal}`;
    }

    // List items  - value
    const listMatch = raw.match(/^(\s*)(- )(.*)$/);
    if (listMatch) {
      const [, indent, dash, rest] = listMatch;
      return `${esc(indent)}<span style="color:#d4d4d4">${esc(dash)}</span><span class="yv">${esc(rest)}</span>`;
    }

    return line;
  }).join('\n');
}

// ── LIVE YAML PREVIEW ──────────────────────────
function liveUpdateYaml() {
  if (!S.platform) return;
  const branch = (document.getElementById('branchInput')?.value || 'main').trim();
  const tfVer = (document.getElementById('tfVersion')?.value || '1.5.0').trim();
  const pushOn = document.getElementById('pushTrig')?.checked ?? true;
  const prOn = document.getElementById('prTrig')?.checked ?? true;
  const envDev = document.getElementById('envDev')?.checked ?? true;
  const envStg = document.getElementById('envStaging')?.checked ?? true;
  const envProd = document.getElementById('envProd')?.checked ?? false;
  const dm = document.querySelector('input[name="dm"]:checked')?.value || 'progressive';

  const envList = [envDev && 'development', envStg && 'staging', envProd && 'production'].filter(Boolean);
  const applyCondition = dm === 'auto'
    ? ''
    : dm === 'manual'
      ? `      if: github.event_name == 'workflow_dispatch'\n`
      : `      if: github.ref == 'refs/heads/${branch}'\n`;

  let yaml = '';

  if (S.platform === 'github') {
    const onBlock = [];
    if (pushOn) onBlock.push(`  push:\n    branches: [${branch}]`);
    if (prOn) onBlock.push(`  pull_request:\n    branches: [${branch}]`);
    if (dm === 'manual') onBlock.push(`  workflow_dispatch: {}`);
    yaml = `name: Terraform CI/CD
on:
${onBlock.join('\n')}

jobs:
  terraform:
    name: Terraform Plan & Apply
    runs-on: ubuntu-latest
    environment: ${envList.join(', ') || 'development'}
    permissions:
      contents: read
      id-token: write

    steps:
      - uses: actions/checkout@v4

      - id: auth
        uses: google-github-actions/auth@v2
        with:
          workload_identity_provider: \${{ secrets.WIF_PROVIDER }}
          service_account: \${{ secrets.SA_EMAIL }}

      - uses: hashicorp/setup-terraform@v3
        with:
          terraform_version: "${tfVer}"

      - name: Terraform Init
        run: terraform init

      - name: Terraform Validate
        run: terraform validate

      - name: Terraform Plan
        run: terraform plan -out=tfplan

      - name: Terraform Apply
${applyCondition || `        if: github.ref == 'refs/heads/${branch}'\n`}        run: terraform apply -auto-approve tfplan`;
  }

  else if (S.platform === 'gitlab') {
    const stages = envList.map(e => `  - ${e}`).join('\n') || '  - plan';
    yaml = `stages:\n${stages}\n\nvariables:\n  TF_VERSION: "${tfVer}"\n\n`;
    yaml += `.tf_base: &tf_base\n  image: hashicorp/terraform:\${TF_VERSION}\n  before_script:\n    - terraform init\n\n`;
    if (pushOn) yaml += `only: [${branch}]\n`;
    yaml += envList.map(e => `${e}:\n  <<: *tf_base\n  stage: ${e}\n  script:\n    - terraform plan\n    - terraform apply -auto-approve\n`).join('\n');
  }

  else if (S.platform === 'cloudbuild') {
    yaml = `timeout: '1800s'\noptions:\n  logging: CLOUD_LOGGING_ONLY\n\nsteps:\n`;
    yaml += `  - name: 'hashicorp/terraform:${tfVer}'\n    id: tf-init\n    entrypoint: terraform\n    args: ['init']\n\n`;
    yaml += `  - name: 'hashicorp/terraform:${tfVer}'\n    id: tf-validate\n    entrypoint: terraform\n    args: ['validate']\n\n`;
    yaml += `  - name: 'hashicorp/terraform:${tfVer}'\n    id: tf-plan\n    entrypoint: terraform\n    args: ['plan', '-out=tfplan']\n\n`;
    if (dm !== 'manual') {
      yaml += `  - name: 'hashicorp/terraform:${tfVer}'\n    id: tf-apply\n    entrypoint: terraform\n    args: ['apply', '-auto-approve', 'tfplan']\n`;
    }
    if (envList.length) yaml += `\nsubstitutions:\n  _ENV: ${envList[envList.length - 1]}`;
  }

  else if (S.platform === 'jenkins') {
    const applyStage = dm === 'manual'
      ? `    stage('Apply') {\n      input { message 'Deploy?' }\n      steps { sh 'terraform apply -auto-approve tfplan' }\n    }`
      : `    stage('Apply') {\n      when { branch '${branch}' }\n      steps { sh 'terraform apply -auto-approve tfplan' }\n    }`;
    yaml = `pipeline {\n  agent any\n  environment {\n    TF_VERSION = '${tfVer}'\n    ENVIRONMENTS = '${envList.join(',')}' \n  }\n  stages {\n    stage('Init') { steps { sh 'terraform init' } }\n    stage('Validate') { steps { sh 'terraform validate' } }\n    stage('Plan') {\n      steps {\n        sh 'terraform plan -out=tfplan'\n        archiveArtifacts 'tfplan'\n      }\n    }\n${applyStage}\n  }\n  post { always { cleanWs() } }\n}`;
  }

  // Apply syntax-highlighted content
  const codeEl = document.getElementById('yamlContent');
  if (codeEl) codeEl.innerHTML = highlightYaml(yaml);

  // Update gutter line numbers
  const gutter = document.getElementById('ypGutter');
  if (gutter) {
    const lineCount = yaml.split('\n').length;
    gutter.innerHTML = Array.from({ length: lineCount }, (_, i) => i + 1).join('\n');
  }

  // Update status bar
  const lineCountEl = document.getElementById('ypLineCount');
  if (lineCountEl) lineCountEl.textContent = `${yaml.split('\n').length} lines`;

  // Sync gutter scroll with code scroll
  if (codeEl && gutter && !codeEl._gutterSynced) {
    codeEl._gutterSynced = true;
    codeEl.addEventListener('scroll', () => { gutter.scrollTop = codeEl.scrollTop; });
  }
}


