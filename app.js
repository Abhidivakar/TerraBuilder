// ── PROMPTS ────────────────────────────────────
function renderServices(q) {
  const grid = document.getElementById('servicesGrid');
  const empty = document.getElementById('emptyState');
  const count = document.getElementById('srchCount');
  const query = (q || '').trim().toLowerCase();

  const SERVICES = SERVICES_BY_PROVIDER[S.provider] || [];
  const filtered = query ? SERVICES.filter(s => s.name.toLowerCase().includes(query)) : SERVICES;

  document.getElementById('emptyQ') && (document.getElementById('emptyQ').textContent = query);
  if (filtered.length === 0) {
    grid.innerHTML = '';
    empty.style.display = 'flex';
    count.textContent = '0 services';
  } else {
    empty.style.display = 'none';
    count.textContent = filtered.length + ' service' + (filtered.length !== 1 ? 's' : '');
    grid.innerHTML = filtered.map(s => `
      <div class="svc-card">
        <div class="svc-top">
          <div class="svc-ico">
            <img src="${s.icon}" alt="${s.name}" width="32" height="32"
              onerror="this.style.display='none';this.nextElementSibling.style.display='flex'" />
            <span class="svc-ico-fallback" style="display:none">☁️</span>
          </div>
          <div class="svc-name">${s.name}</div>
        </div>
        <div class="svc-btns">
          <button class="svc-dl1" onclick="dlPrompt('${s.name}','v1')">V1 Basic</button>
          <button class="svc-dl2" onclick="dlPrompt('${s.name}','v2')">V2 Module</button>
        </div>
      </div>
    `).join('');
  }
}

