// ── BUILD: GENERATE ────────────────────────────
function generateTerraform() {
  if (S.resources.length === 0) { showToast('Add at least one resource', 'err'); return; }
  showStage('generating');
  setTrail(4);
  setNavStatus(true, 'Generating…');

  const el = document.getElementById('genCodePreview');
  el.textContent = '';
  let i = 0;

  const lines = [
    ...(GEN_LINES_BY_PROVIDER[S.provider] || GEN_LINES_BY_PROVIDER.gcp),
    '',
    '# ── Resources ──────────────────────────────────',
    ...S.resources.flatMap(r => [
      '',
      `module "${r.replace(/-/g, '_')}" {`,
      `  source    = "./modules/${r}"`,
      `  project   = var.project_id`,
      `  region    = var.region`,
      `  env       = "${S.env}"`,
      `}`,
    ]),
  ];

  const tick = setInterval(() => {
    if (i < lines.length) {
      el.textContent += lines[i] + '\n';
      el.scrollTop = el.scrollHeight;
      i++;
    } else {
      clearInterval(tick);
      setTimeout(showDoneStage, 500);
    }
  }, 55);
}

