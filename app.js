function setFile(f) {
  const allowed = ['image/png', 'image/jpeg', 'image/jpg', 'application/pdf'];
  if (!allowed.includes(f.type)) { showToast('PNG, JPG, or PDF only', 'err'); return; }
  if (f.size > 10 * 1024 * 1024) { showToast('File must be under 10MB', 'err'); return; }
  S.file = f;

  const area = document.getElementById('filePreviewArea');
  area.innerHTML = '';
  if (f.type.startsWith('image/')) {
    const r = new FileReader();
    r.onload = ev => { area.innerHTML = `<img src="${ev.target.result}" alt="preview"/>`; };
    r.readAsDataURL(f);
  } else {
    area.innerHTML = `<div style="padding:32px;color:var(--t2);text-align:center">📄 ${f.name}</div>`;
  }
  document.getElementById('fileName').textContent = f.name;
  document.getElementById('fileSize').textContent = (f.size / 1024).toFixed(0) + ' KB';
  document.getElementById('sdContent').style.display = 'none';
  document.getElementById('sdSelected').style.display = 'block';
}

