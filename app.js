let _termTimer = null;
function startTerminal() {
  const body = document.getElementById('termBody');
  if (!body) return;
  body.innerHTML = '';
  clearTimeout(_termTimer);
  let i = 0;
  let currentLine = null;

  function step() {
    if (i >= TERM_SCRIPT.length) {
      // Restart after a delay
      _termTimer = setTimeout(startTerminal, 3000);
      return;
    }
    const entry = TERM_SCRIPT[i++];
    if (entry.cls === 'tl-prompt') {
      // Start new line with prompt + next word
      const nextEntry = TERM_SCRIPT[i] || {};
      currentLine = document.createElement('div');
      currentLine.className = 'term-line';
      const promptSpan = document.createElement('span');
      promptSpan.className = 'tl-prompt';
      promptSpan.textContent = entry.t;
      currentLine.appendChild(promptSpan);
      const cmdSpan = document.createElement('span');
      cmdSpan.className = 'tl-cmd';
      currentLine.appendChild(cmdSpan);
      body.appendChild(currentLine);
      // Typewrite the next entry (cmd)
      let j = 0;
      const cmd = nextEntry.t || '';
      i++; // skip the cmd entry
      function type() {
        if (j < cmd.length) {
          cmdSpan.textContent += cmd[j++];
          _termTimer = setTimeout(type, 35);
        } else {
          _termTimer = setTimeout(step, nextEntry.pause || 200);
        }
      }
      _termTimer = setTimeout(type, 60);
    } else {
      const line = document.createElement('div');
      line.className = 'term-line';
      const span = document.createElement('span');
      span.className = entry.cls;
      span.textContent = entry.t;
      line.appendChild(span);
      body.appendChild(line);
      body.scrollTop = body.scrollHeight;
      _termTimer = setTimeout(step, entry.pause || 120);
    }
  }
  step();
}
